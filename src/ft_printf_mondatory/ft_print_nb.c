#include "../../lib/ft_printf.h"

int	ft_print_nb(long nb)
{
	int	nbr_len;

	nbr_len = 1;
	if (nb < 0)
	{
		nb *= -1;
		nbr_len++;
	}
	while (nb >= 10)
	{
		nb = nb / 10;
		nbr_len++;
	}
	return (nbr_len);
}
